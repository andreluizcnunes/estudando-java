package com.aulas.aula05;

public class OperadoresRelacionais {
    public static void main(String[] args) {
//        == igual a
//        != diferente de
//        > maior que
//        < menor que
//        >= maior ou igual que
//        <= menor ou igual que

        int valor1 = 1;
        int valor2 = 2;

        System.out.println("valor1 == valor2: "+ (valor1 == valor2));
        System.out.println("valor1 != valor2: "+ (valor1 != valor2));
        System.out.println("valor1 > valor2: "+ (valor1 > valor2));
        System.out.println("valor1 < valor2: "+ (valor1 < valor2));
        System.out.println("valor1 >= valor2: "+ (valor1 >= valor2));
        System.out.println("valor1 <= valor2: "+ (valor1 <= valor2));
    }
}
