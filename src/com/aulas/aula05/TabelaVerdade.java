package com.aulas.aula05;

public class TabelaVerdade {
    public static void main(String[] args) {
        // || - OR curto circuito
        // && - AND curto circuito

        // Tabela Verdade

        /*
        Usando & - AND

        false & false = false
        true & false = false
        false & true = false
        true & true = true
        * */

        /*
        Usando | - OR

        false | false = false
        true | false = true
        false | true = true
        true | true = true
        * */

        /*
        Usando ^ - XOR

        false ^ false = false
        true ^ false = true
        false ^ true = true
        true ^ true = false
        * */

        /*
        Usando ! - NOT

        !false = true
        !true = false
        * */
    }
}
